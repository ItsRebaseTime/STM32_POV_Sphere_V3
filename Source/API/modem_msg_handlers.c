void MSG_EIND (sMsgHandlerArgs_t *handler_args) {
    uint16_t code = atoi(handler_args->msg_args[0]);
    snprintf(handler_args->response_buffer, handler_args->response_buffer_size, "EIND %d\r\n", code);
}

