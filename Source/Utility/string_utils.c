void remchar (char *string, const char *char_list, uint16_t length) {
    uint16_t bad_chars_count = 0;
    for (uint16_t i = 0; i <= length; i++) {
        if (bad_chars_count > 0) {
            string[i - bad_chars_count] = string[i];
        }
        for (uint16_t c = 0; c < strlen(char_list); c++) {
            if (string[i] == char_list[c]) {
                bad_chars_count++;
            }
        }
    }
}

int strlcmp (const char *str1, const char *str2) {
    if (strlen(str1) == strlen(str2)) {
        return strncmp(str1, str2, strlen(str2));
    }
    return -1;
}
